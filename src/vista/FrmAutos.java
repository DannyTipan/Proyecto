    private void cmbStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbStockActionPerformed
        String stockSeleccionado = cmbStock.getSelectedItem().toString();
        switch (stockSeleccionado) {
            case "Color":
                activarComponentesParaColor();
                break;
            case "Marca":
                activarComponentesParaMarca();
                break;
            case "Tipo":
                activarComponentesParaTipo();
                break;

        }

    }//GEN-LAST:event_cmbStockActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        limpiarTablaDeAutos();
        boolean encontrado = false;
        for (Auto a : listaDeAutos) {
            if (a.getColor().toLowerCase().equals(txtColor.getText().toLowerCase())) {
                agregarAutoALaTabla(a);
                encontrado = true;
            } else if (a.getMarca().toLowerCase().equals(txtMarca.getText().toLowerCase())) {
                agregarAutoALaTabla(a);
                encontrado = true;
            } else if (a.getTipo().toLowerCase().equals(txtTipo.getText().toLowerCase())) {
                agregarAutoALaTabla(a);
                encontrado = true;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(rootPane, "Auto no disponible");
        }


    }//GEN-LAST:event_btnBuscarActionPerformed
    private void limpiarTablaDeAutos() {
        for (int i = 0; i < tblAutos.getRowCount(); i++) {
            dtmAutos.removeRow(i);
            i -= 1;

        }

    }

