    private void guardarAutoSeleccionado() {
        int[] filasSeleccionadas = tblAutos.getSelectedRows();
        // Iterar sobre las filas seleccionadas
        for (int fila : filasSeleccionadas) {
            // Obtener el modelo de la tabla y la fila correspondiente a la selección
            dtmAutos = (DefaultTableModel) tblAutos.getModel();
            Object[] filaSeleccionada = new Object[dtmAutos.getColumnCount()];

            for (int i = 0; i < dtmAutos.getColumnCount(); i++) {
                filaSeleccionada[i] = dtmAutos.getValueAt(fila, i);
            }

            // Crear un objeto Auto a partir de la fila seleccionada y añadirlo al ArrayList
            Auto autoSeleccionado = new Auto(filaSeleccionada[0].toString(), filaSeleccionada[1].toString(), filaSeleccionada[2].toString(), Double.parseDouble(filaSeleccionada[3].toString()));
            listaDeAutosSeleccionados.add(autoSeleccionado);
        }

    }


